package ex_04_Operators;

public class Lab054_Interview_Char_INT {
    public static void main(String[] args) {
        char a1 ='A'; //ASCII ->65
        char b1 = 'B'; //ASCII -> 66
        System.out.println(a1+b1);
    /* n Java, char is a data type that holds a single character,
    like 'A' or 'B'.
     'A' has an ASCII (or Unicode) value of 65
     'B' has an ASCII value of 66
     When you add two char values, Java converts them to integers (their ASCII values) and then adds them
     so, 65+66 =131
     */


    }
}
